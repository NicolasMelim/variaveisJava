package operadoresRelacionais;

public class operadoresRelacionais {
public static void main(String[] args) {
	int num2 = 10;
	int num3 = 12;
	
	if(num2 == num3)
		System.out.print("Numeros iguais");
	else if(num2 != num3)
		System.out.print("Nuermos diferentes");
	else
		System.out.print("Numero menor que o outro");
}
}
